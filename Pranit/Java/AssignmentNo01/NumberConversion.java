package number.conversion;

public class NumberConversion {


	public static void main(String[] args) {
		
		int Number = 20;
		
		System.out.println("Enter Number : 20");
		
		System.out.println("Given Number : 20");
		
		System.out.println("Binary Equivalent : "+ Integer.toBinaryString(Number));
		
		System.out.println("Octale Equivalent : "+ Integer.toOctalString(Number));
		
		System.out.println("Hexadecimal Equivalent : "+ Integer.toHexString(Number));
		
		
		

	}

}
